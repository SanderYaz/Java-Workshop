
public class ComparisonDemo {

	
    public static void main(String[] args){
    	
        int value1 = 3;
        int value2 = 2;
        
        if(value1 == value2)
            System.out.println("value1 == value2");
        
        if(value1 != value2)
            System.out.println("value1 != value2");
        
        if(value1 > value2)
            System.out.println("value1 > value2");
        
        if(value1 < value2)
            System.out.println("value1 < value2");
        
        if(value1 <= value2)
            System.out.println("value1 <= value2");
    }
    
}
